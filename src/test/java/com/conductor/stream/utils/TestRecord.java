package com.conductor.stream.utils;

/**
 * A simple record object to be used in the join tests.
 *
 * @author Benjamin Shai
 */
public class TestRecord {

    private final int id;
    private final String value;

    public TestRecord(int id, String value) {
        this.id = id;
        this.value = value;
    }

    public int getId() {
        return id;
    }

    public String getValue() {
        return value;
    }


}
